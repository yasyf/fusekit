func goType(f field) string {
	typeName := map[string]string{
		"uint16": "uint16", "uint32": "uint32", "uint64": "uint64",
		"string": "string", "bool": "bool", "bytes": "[]byte",
	}[f.Type]
	if typeName == "" {
		typeName = f.Type
	}
	if f.Array {
		typeName = "[]" + typeName
	}
	if f.Optional {
		typeName = "*" + typeName
	}
	return typeName
}

