func schemaBuild() string {
	var schema strings.Builder
	fmt.Fprintf(&schema, "version:%d\n", protocolVersion)
	fmt.Fprintf(&schema, "max-page-size:%d\n", maxPageSize)
	fmt.Fprintf(&schema, "max-signal-targets:%d\n", maxSignalTargets)
	fmt.Fprintf(&schema, "max-name-bytes:%d\n", maxNameBytes)
	fmt.Fprintf(&schema, "max-broker-domain-page-size:%d\n", maxBrokerDomainPageSize)
	fmt.Fprintf(&schema, "max-broker-forward-payload-bytes:%d\n", maxBrokerForwardPayloadBytes)
	fmt.Fprintf(&schema, "max-error-message-bytes:%d\n", maxErrorMessageBytes)
	fmt.Fprintf(&schema, "max-display-name-bytes:%d\n", maxDisplayNameBytes)
	fmt.Fprintf(&schema, "max-public-path-bytes:%d\n", maxPublicPathBytes)
	fmt.Fprintf(&schema, "max-source-fleet-declarations:%d\n", maxSourceFleetDeclarations)
	fmt.Fprintf(&schema, "max-source-fleet-bytes:%d\n", maxSourceFleetBytes)
	fmt.Fprintf(&schema, "max-source-driver-id-bytes:%d\n", maxSourceDriverIDBytes)
	fmt.Fprintf(&schema, "max-source-driver-config-bytes:%d\n", maxSourceDriverConfigBytes)
	for _, enum := range enums {
		fmt.Fprintf(&schema, "enum:%s:%s\n", enum.Name, strings.Join(enum.Values, ","))
	}
	for _, message := range messages {
		fmt.Fprintf(&schema, "message:%s\n", message.Name)
		for _, field := range message.Fields {
			fmt.Fprintf(&schema, "field:%s:%s:%s:%s:%t:%t\n", field.JSON, field.Go, field.Swift, field.Type, field.Optional, field.Array)
		}
	}
	digest := sha256.Sum256([]byte(schema.String()))
	return "fusekit.catalog." + hex.EncodeToString(digest[:])
}

func renderSwiftMessage(b *strings.Builder, m message) {
	fmt.Fprintf(b, "public struct %s: Codable, Sendable {\n", swiftMessageName(m.Name))
	for _, f := range m.Fields {
		fmt.Fprintf(b, "    public let %s: %s\n", f.Swift, swiftType(f))
	}
	b.WriteString("\n    private enum CodingKeys: String, CodingKey {\n")
	for _, f := range m.Fields {
		fmt.Fprintf(b, "        case %s = %q\n", f.Swift, f.JSON)
	}
	b.WriteString("    }\n\n")
	throws := ""
	if swiftShapeValidation(m.Name, "") != "" {
		throws = " throws"
	}
	fmt.Fprintf(b, "    public init(%s)%s {\n", swiftParameters(m.Fields), throws)
	for _, f := range m.Fields {
		fmt.Fprintf(b, "        self.%s = %s\n", f.Swift, f.Swift)
	}
	b.WriteString(swiftShapeValidation(m.Name, "        "))
	b.WriteString("    }\n\n")
	b.WriteString("    public init(from decoder: Decoder) throws {\n")
	allowed := make([]string, 0, len(m.Fields))
	for _, f := range m.Fields {
		allowed = append(allowed, fmt.Sprintf("%q", f.JSON))
	}
	sort.Strings(allowed)
	fmt.Fprintf(b, "        try catalogValidateKeys(decoder, allowed: [%s])\n", strings.Join(allowed, ", "))
	b.WriteString("        let container = try decoder.container(keyedBy: CodingKeys.self)\n")
	for _, f := range m.Fields {
		method := "decode"
		if f.Optional {
			method = "decodeIfPresent"
		}
		base := f
		base.Optional = false
		fmt.Fprintf(b, "        %s = try container.%s(%s.self, forKey: .%s)\n", f.Swift, method, swiftType(base), f.Swift)
	}
	for _, f := range m.Fields {
		if f.JSON == "protocol" {
			fmt.Fprintf(b, "        guard %s == CatalogProtocol.version else { throw CatalogProtocolCodingError.unsupportedProtocol(%s) }\n", f.Swift, f.Swift)
		}
	}
	if isResponse(m) && m.Name != "BrokerResult" {
		b.WriteString("        guard (code == .ok) == message.isEmpty else { throw CatalogProtocolCodingError.invalidShape(\"response message does not match code\") }\n")
		b.WriteString("        guard message.utf8.count <= Int(CatalogProtocol.maxErrorMessageBytes) else { throw CatalogProtocolCodingError.invalidShape(\"response message is outside bounds\") }\n")
	}
	b.WriteString(swiftShapeValidation(m.Name, "        "))
	b.WriteString("    }\n")
	b.WriteString("}\n\n")
}

func isResponse(m message) bool {
	hasCode := false
	hasMessage := false
	for _, field := range m.Fields {
		hasCode = hasCode || field.JSON == "code"
		hasMessage = hasMessage || field.JSON == "message"
	}
	return hasCode && hasMessage
}

func swiftShapeValidation(name, indent string) string {
	var lines []string
	switch name {
	case "CatalogObject":
		lines = []string{
			"try catalogValidateName(name)",
			"guard size <= UInt64(Int64.max) else { throw CatalogProtocolCodingError.invalidShape(\"object size exceeds signed presentation range\") }",
			"switch kind {",
			"case .directory: guard contentRevision == 0, size == 0, hash.isEmpty, linkTarget.isEmpty else { throw CatalogProtocolCodingError.invalidShape(\"directory content\") }",
			"case .file: guard contentRevision != 0, linkTarget.isEmpty else { throw CatalogProtocolCodingError.invalidShape(\"file content\") }",
			"case .symlink:",
			"    try catalogValidateLinkTarget(linkTarget)",
			"    guard contentRevision != 0, size == linkTarget.utf8.count, hash == catalogSymlinkHash(linkTarget) else { throw CatalogProtocolCodingError.invalidShape(\"symlink content\") }",
			"}",
		}
	case "SignalTarget":
		lines = []string{
			"switch kind {",
			"case .workingSet: guard parentID == nil else { throw CatalogProtocolCodingError.invalidShape(\"working_set target carries parent_id\") }",
			"case .container: guard parentID != nil else { throw CatalogProtocolCodingError.invalidShape(\"container target requires parent_id\") }",
			"}",
		}
	case "EnumerationScope":
		lines = []string{
			"switch kind {",
			"case .workingSet: guard parentID == nil else { throw CatalogProtocolCodingError.invalidShape(\"working_set scope carries parent_id\") }",
			"case .container: guard parentID != nil else { throw CatalogProtocolCodingError.invalidShape(\"container scope requires parent_id\") }",
			"}",
		}
	case "MutationRequest":
		lines = []string{
			"if let name { try catalogValidateName(name) }",
			"switch kind {",
			"case .create:",
			"    guard objectKind != nil, objectID == nil, parentID != nil, targetID == nil, name != nil, mode != nil else { throw CatalogProtocolCodingError.invalidShape(\"create mutation shape\") }",
			"    if objectKind == .directory { guard !hasContent, contentRevision == nil, linkTarget == nil else { throw CatalogProtocolCodingError.invalidShape(\"directory create content\") } }",
			"    if objectKind == .file { guard hasContent, contentRevision != nil, contentRevision != 0, linkTarget == nil else { throw CatalogProtocolCodingError.invalidShape(\"file create content\") } }",
			"    if objectKind == .symlink { guard !hasContent, contentRevision != nil, contentRevision != 0, let linkTarget else { throw CatalogProtocolCodingError.invalidShape(\"symlink create content\") }; try catalogValidateLinkTarget(linkTarget) }",
			"case .revise:",
			"    guard objectKind == nil, objectID != nil, parentID != nil, targetID == nil, name != nil, mode != nil, linkTarget == nil, hasContent == (contentRevision != nil), contentRevision == nil || contentRevision != 0 else { throw CatalogProtocolCodingError.invalidShape(\"revise mutation shape\") }",
			"case .delete:",
			"    guard objectKind == nil, !hasContent, objectID != nil, parentID == nil, targetID == nil, name == nil, mode == nil, contentRevision == nil, linkTarget == nil else { throw CatalogProtocolCodingError.invalidShape(\"delete mutation shape\") }",
			"case .replace:",
			"    guard objectKind == nil, objectID != nil, targetID != nil, linkTarget == nil, hasContent == (contentRevision != nil), contentRevision == nil || contentRevision != 0 else { throw CatalogProtocolCodingError.invalidShape(\"replace mutation shape\") }",
			"}",
		}
	case "MutationResponse":
		lines = []string{
			"if code == .ok {",
			"    guard requestID != nil, let mutationID, revision != 0 else { throw CatalogProtocolCodingError.invalidShape(\"successful mutation response has no identity\") }",
			"    guard UInt64(mutationID.rawValue.prefix(16), radix: 16) == revision else { throw CatalogProtocolCodingError.invalidShape(\"mutation id target revision does not match response\") }",
			"}",
		}
	case "DomainRegistration", "RegisteredDomain":
		lines = []string{
			"guard domainID == CatalogDomainID.derived(ownerID: ownerID, presentationInstanceID: presentationInstanceID) else { throw CatalogProtocolCodingError.invalidShape(\"domain id is not derived from owner and presentation instance\") }",
			"guard generation != 0 else { throw CatalogProtocolCodingError.invalidShape(\"domain generation is zero\") }",
			"guard !displayName.isEmpty, displayName.utf8.count <= Int(CatalogProtocol.maxDisplayNameBytes) else { throw CatalogProtocolCodingError.invalidShape(\"domain display name is outside bounds\") }",
		}
		if name == "RegisteredDomain" {
			lines = append(lines,
				"guard !publicPath.isEmpty, publicPath.utf8.count <= Int(CatalogProtocol.maxPublicPathBytes) else { throw CatalogProtocolCodingError.invalidShape(\"domain public path is outside bounds\") }",
			)
		}
	case "SourceAuthorityDeclaration":
		lines = []string{
			"try catalogValidateSourceDriverID(driverID)",
			"guard driverConfig.count <= Int(CatalogProtocol.maxSourceDriverConfigBytes) else { throw CatalogProtocolCodingError.invalidShape(\"source driver config is outside bounds\") }",
			"try catalogValidateDigest(declarationDigest)",
		}
	case "DesiredSourceFleetState":
		lines = []string{
			"try catalogValidateSourceIdentity(owner)",
			"guard generation != 0, authorityCount <= UInt64(CatalogProtocol.maxSourceFleetDeclarations) else { throw CatalogProtocolCodingError.invalidShape(\"desired source fleet state is outside bounds\") }",
			"try catalogValidateDigest(authoritiesDigest)",
			"try catalogValidateDigest(declarationsDigest)",
		}
	case "PublishDesiredSourceFleetRequest":
		lines = []string{
			"try catalogValidateSourceIdentity(owner)",
			"guard generation != 0, generation > expectedGeneration, declarations.count <= Int(CatalogProtocol.maxSourceFleetDeclarations) else { throw CatalogProtocolCodingError.invalidShape(\"desired source fleet request is outside bounds\") }",
			"guard declarations.elementsEqual(declarations.sorted { $0.authority.rawValue < $1.authority.rawValue }, by: { $0.authority == $1.authority }), Set(declarations.map(\\.authority)).count == declarations.count else { throw CatalogProtocolCodingError.invalidShape(\"source authority declarations are not sorted and unique\") }",
			"let declarationBytes = declarations.reduce(0) { $0 + $1.authority.rawValue.utf8.count + $1.driverID.utf8.count + $1.driverConfig.count + 32 }",
			"guard declarationBytes <= Int(CatalogProtocol.maxSourceFleetBytes) else { throw CatalogProtocolCodingError.invalidShape(\"desired source fleet exceeds byte budget\") }",
		}
	case "PublishDesiredSourceFleetResponse":
		lines = []string{
			"guard (code == .ok) == (state != nil) else { throw CatalogProtocolCodingError.invalidShape(\"desired source fleet response state does not match result\") }",
		}
	case "ReadDesiredSourceFleetRequest":
		lines = []string{
			"try catalogValidateSourceIdentity(owner)",
			"guard limit != 0, limit <= CatalogProtocol.maxSourceFleetDeclarations else { throw CatalogProtocolCodingError.invalidShape(\"desired source fleet page limit is outside bounds\") }",
			"if generation == 0 { guard snapshotDigest == nil, after == nil else { throw CatalogProtocolCodingError.invalidShape(\"head desired source fleet request carries a snapshot cursor\") } } else { guard let snapshotDigest else { throw CatalogProtocolCodingError.invalidShape(\"pinned desired source fleet request has no snapshot digest\") }; try catalogValidateDigest(snapshotDigest) }",
		}
	case "ReadDesiredSourceFleetResponse":
		lines = []string{
			"guard (code == .ok) == (state != nil), code == .ok || (declarations.isEmpty && next == nil) else { throw CatalogProtocolCodingError.invalidShape(\"desired source fleet page does not match result\") }",
			"guard declarations.count <= Int(CatalogProtocol.maxSourceFleetDeclarations), declarations.elementsEqual(declarations.sorted { $0.authority.rawValue < $1.authority.rawValue }, by: { $0.authority == $1.authority }), Set(declarations.map(\\.authority)).count == declarations.count else { throw CatalogProtocolCodingError.invalidShape(\"desired source fleet page is not ordered and bounded\") }",
			"if let next { guard declarations.last?.authority == next else { throw CatalogProtocolCodingError.invalidShape(\"desired source fleet continuation does not match final declaration\") } }",
			"let declarationBytes = declarations.reduce(0) { $0 + $1.authority.rawValue.utf8.count + $1.driverID.utf8.count + $1.driverConfig.count + 32 }",
			"guard declarationBytes <= Int(CatalogProtocol.maxSourceFleetBytes) else { throw CatalogProtocolCodingError.invalidShape(\"desired source fleet page exceeds byte budget\") }",
		}
	case "BrokerForwardRequest":
		lines = []string{
			"guard !payload.isEmpty, payload.count <= Int(CatalogProtocol.maxBrokerForwardPayloadBytes) else { throw CatalogProtocolCodingError.invalidShape(\"broker forward payload is outside bounds\") }",
		}
	case "BrokerCommand":
		lines = []string{
			"guard commandID != 0 else { throw CatalogProtocolCodingError.invalidShape(\"zero broker command id\") }",
			"switch kind {",
			"case .registerDomain: guard registration != nil, domainID == nil, notification == nil, afterDomainID == nil else { throw CatalogProtocolCodingError.invalidShape(\"register_domain command shape\") }",
			"case .removeDomain: guard registration == nil, domainID != nil, notification == nil, afterDomainID == nil else { throw CatalogProtocolCodingError.invalidShape(\"remove_domain command shape\") }",
			"case .listDomains: guard registration == nil, domainID == nil, notification == nil else { throw CatalogProtocolCodingError.invalidShape(\"list_domains command shape\") }",
			"case .signalDomain: guard registration == nil, domainID == nil, notification != nil, afterDomainID == nil else { throw CatalogProtocolCodingError.invalidShape(\"signal_domain command shape\") }",
			"}",
		}
	case "BrokerResult":
		lines = []string{
			"guard message.utf8.count <= Int(CatalogProtocol.maxErrorMessageBytes) else { throw CatalogProtocolCodingError.invalidShape(\"broker result message is outside bounds\") }",
			"guard (code == .ok) == message.isEmpty else { throw CatalogProtocolCodingError.invalidShape(\"broker result message does not match code\") }",
			"if let domains { guard domains.count <= Int(CatalogProtocol.maxBrokerDomainPageSize) else { throw CatalogProtocolCodingError.invalidShape(\"broker domain page is outside bounds\") } }",
			"if let nextAfterDomainID { guard kind == .listDomains, let domains, domains.count == Int(CatalogProtocol.maxBrokerDomainPageSize), domains.last?.domainID == nextAfterDomainID else { throw CatalogProtocolCodingError.invalidShape(\"broker domain page cursor is invalid\") } }",
		}
	}
	if len(lines) == 0 {
		return ""
	}
	return indent + strings.Join(lines, "\n"+indent) + "\n"
}

func swiftParameters(fields []field) string {
	parameters := make([]string, 0, len(fields))
	for _, f := range fields {
		value := fmt.Sprintf("%s: %s", f.Swift, swiftType(f))
		if f.JSON == "protocol" {
			value += " = CatalogProtocol.version"
		} else if f.Optional {
			value += " = nil"
		}
		parameters = append(parameters, value)
	}
	return strings.Join(parameters, ", ")
}

func swiftType(f field) string {
	typeName := map[string]string{
		"uint16": "UInt16", "uint32": "UInt32", "uint64": "UInt64", "int64": "Int64",
		"string": "String", "bool": "Bool", "bytes": "Data", "ObjectID": "CatalogObjectID",
		"MutationRequestID": "CatalogMutationRequestID", "MutationID": "CatalogMutationID",
		"OperationID": "CatalogOperationID", "TenantID": "CatalogTenantID",
		"DomainID": "CatalogDomainID", "OwnerID": "CatalogOwnerID", "PresentationInstanceID": "CatalogPresentationInstanceID",
		"SourceAuthorityID": "CatalogSourceAuthorityID", "ChangeID": "CatalogChangeID",
	}[f.Type]
	if typeName == "" {
		typeName = swiftMessageName(f.Type)
	}
	if f.Array {
		typeName = "[" + typeName + "]"
	}
	if f.Optional {
		typeName += "?"
	}
	return typeName
}

func swiftMessageName(name string) string {
	if strings.HasPrefix(name, "Catalog") {
		return name
	}
	return "Catalog" + name
}

func exported(value string) string {
	parts := strings.FieldsFunc(value, func(r rune) bool { return r == '_' || r == '.' })
	for i := range parts {
		parts[i] = strings.ToUpper(parts[i][:1]) + parts[i][1:]
	}
	return strings.Join(parts, "")
}

func swiftCase(value string) string {
	name := exported(value)
	return strings.ToLower(name[:1]) + name[1:]
}
