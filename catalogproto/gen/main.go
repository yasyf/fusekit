const generatedHeader = "Code generated by catalogproto/gen; DO NOT EDIT."
const protocolVersion uint16 = 1
const maxPageSize uint32 = 1_000
const maxSignalTargets uint32 = 64
const maxNameBytes uint32 = 255
const maxBrokerDomainPageSize uint32 = 16
const maxBrokerForwardPayloadBytes uint32 = 1 << 20
const maxErrorMessageBytes uint32 = 4 << 10
const maxDisplayNameBytes uint32 = 255
const maxPublicPathBytes uint32 = 4 << 10
const maxSourceFleetDeclarations uint32 = 256
const maxSourceFleetBytes uint32 = 1 << 20
const maxSourceDriverIDBytes uint32 = 128
const maxSourceDriverConfigBytes uint32 = 64 << 10

func main() {
	check := flag.Bool("check", false, "fail if generated files differ")
	flag.Parse()
	root := moduleRoot()
	goSource, err := format.Source([]byte(renderGo()))
	if err != nil {
		panic(err)
	}
	swiftSource, err := formatSwift([]byte(renderSwift()))
	if err != nil {
		panic(err)
	}
	outputs := map[string][]byte{
		filepath.Join(root, "catalogproto", "messages_gen.go"):                                    goSource,
		filepath.Join(root, "Sources", "FuseKit", "Generated", "CatalogProtocol.generated.swift"): swiftSource,
	}
	for path, content := range outputs {
		if *check {
			existing, err := os.ReadFile(path)
			if err != nil || !bytes.Equal(existing, content) {
				fmt.Fprintf(os.Stderr, "%s is stale\n", path)
				os.Exit(1)
			}
			continue
		}
		if err := os.MkdirAll(filepath.Dir(path), 0o755); err != nil {
			panic(err)
		}
		if err := os.WriteFile(path, content, 0o644); err != nil {
			panic(err)
		}
	}
}

func formatSwift(source []byte) ([]byte, error) {
	command := exec.Command("swift", "format", "-")
	command.Stdin = bytes.NewReader(source)
	var stdout, stderr bytes.Buffer
	command.Stdout = &stdout
	command.Stderr = &stderr
	if err := command.Run(); err != nil {
		return nil, fmt.Errorf("catalogproto/gen: swift format: %w: %s", err, strings.TrimSpace(stderr.String()))
	}
	return stdout.Bytes(), nil
}

func moduleRoot() string {
	_, file, _, ok := runtime.Caller(0)
	if !ok {
		panic("catalogproto/gen: caller path unavailable")
	}
	return filepath.Clean(filepath.Join(filepath.Dir(file), "..", ".."))
}

