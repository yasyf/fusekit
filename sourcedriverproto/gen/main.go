const schemaFingerprintPrefix = "fusekit.sourcedriver."

var errGeneratedOutputStale = errors.New("sourcedriverproto: generated output is stale")

func main() {
	check := flag.Bool("check", false, "verify generated output")
	flag.Parse()
	path := filepath.Join(moduleRoot(), "sourcedriverproto", "messages_gen.go")
	if err := run(*check, wireSpec, path); errors.Is(err, errGeneratedOutputStale) {
		fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	} else if err != nil {
		panic(err)
	}
}

func run(check bool, definition, path string) error {
	generated, _, err := generate(definition)
	if err != nil {
		return err
	}
	if check {
		current, err := os.ReadFile(path)
		if err != nil || !bytes.Equal(current, generated) {
			return errGeneratedOutputStale
		}
		return nil
	}
	return os.WriteFile(path, generated, 0o644)
}

func generate(definition string) ([]byte, string, error) {
	canonical, declarations, err := canonicalWireSpec(definition)
	if err != nil {
		return nil, "", err
	}
	digest := sha256.Sum256(canonical)
	fingerprint := schemaFingerprintPrefix + hex.EncodeToString(digest[:])
	generated, err := format.Source([]byte(fmt.Sprintf(`// Code generated by sourcedriverproto/gen; DO NOT EDIT.

package sourcedriverproto

const (
	SchemaFingerprint = %q
	Build = SchemaFingerprint
)

%s`, fingerprint, declarations)))
	if err != nil {
		return nil, "", err
	}
	return generated, fingerprint, nil
}

func canonicalWireSpec(definition string) ([]byte, []byte, error) {
	const packageClause = "package sourcedriverproto\n\n"
	source := []byte(packageClause + definition)
	file, err := parser.ParseFile(token.NewFileSet(), "wire_spec.go", source, parser.SkipObjectResolution)
	if err != nil {
		return nil, nil, err
	}
	for _, declaration := range file.Decls {
		generic, ok := declaration.(*ast.GenDecl)
		if !ok || generic.Tok != token.CONST && generic.Tok != token.TYPE {
			return nil, nil, errors.New("sourcedriverproto: wire spec permits only const and type declarations")
		}
		for _, item := range generic.Specs {
			switch spec := item.(type) {
			case *ast.TypeSpec:
				if spec.Name.Name == "SchemaFingerprint" || spec.Name.Name == "Build" {
					return nil, nil, errors.New("sourcedriverproto: generated identity cannot be declared by the wire spec")
				}
			case *ast.ValueSpec:
				for _, name := range spec.Names {
					if name.Name == "SchemaFingerprint" || name.Name == "Build" {
						return nil, nil, errors.New("sourcedriverproto: generated identity cannot be declared by the wire spec")
					}
				}
			}
		}
	}
	canonical, err := format.Source(source)
	if err != nil {
		return nil, nil, err
	}
	declarations, ok := bytes.CutPrefix(canonical, []byte(packageClause))
	if !ok {
		return nil, nil, errors.New("sourcedriverproto: canonical wire spec lost its package clause")
	}
	return canonical, declarations, nil
}

func moduleRoot() string {
	_, file, _, ok := runtime.Caller(0)
	if !ok {
		panic("cannot locate generator")
	}
	return filepath.Clean(filepath.Join(filepath.Dir(file), "..", ".."))
}

