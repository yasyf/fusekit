package main

import (
	"bytes"
	"crypto/sha256"
	"encoding/hex"
	"flag"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"runtime"

	"github.com/yasyf/fusekit/catalogproto"
	"github.com/yasyf/fusekit/catalogworker"
	"github.com/yasyf/fusekit/mountproto"
	"github.com/yasyf/fusekit/sourcedriverproto"
)

const suiteVersion uint16 = 1

func main() {
	check := flag.Bool("check", false, "fail when generated files differ")
	flag.Parse()
	root := moduleRoot()
	build := suiteBuild(catalogproto.SchemaFingerprint, catalogworker.SchemaFingerprint, mountproto.SchemaFingerprint, sourcedriverproto.SchemaFingerprint)
	goSource, err := format.Source([]byte(fmt.Sprintf(`// Code generated by transportproto/gen; DO NOT EDIT.

package transportproto

const Version uint16 = %d
const Build = %q
const CatalogSchemaFingerprint = %q
const CatalogWorkerSchemaFingerprint = %q
const MountSchemaFingerprint = %q
const SourceDriverSchemaFingerprint = %q
`, suiteVersion, build, catalogproto.SchemaFingerprint, catalogworker.SchemaFingerprint, mountproto.SchemaFingerprint, sourcedriverproto.SchemaFingerprint)))
	if err != nil {
		panic(err)
	}
	swiftSource := []byte(fmt.Sprintf(`// Code generated by transportproto/gen; DO NOT EDIT.

public enum FuseKitTransportProtocol {
  public static let version: UInt16 = %d
  public static let daemonkitBuild = %q
}
`, suiteVersion, build))
	outputs := map[string][]byte{
		filepath.Join(root, "transportproto", "identity_gen.go"):                                    goSource,
		filepath.Join(root, "Sources", "FuseKit", "Generated", "TransportProtocol.generated.swift"): swiftSource,
	}
	for path, source := range outputs {
		if *check {
			current, err := os.ReadFile(path)
			if err != nil || !bytes.Equal(current, source) {
				fmt.Fprintf(os.Stderr, "%s is stale; run go generate ./transportproto\n", path)
				os.Exit(1)
			}
			continue
		}
		if err := os.WriteFile(path, source, 0o644); err != nil {
			panic(err)
		}
	}
}

func suiteBuild(catalog, catalogWorker, mount, sourceDriver string) string {
	raw := fmt.Sprintf(
		"version:%d\ncatalog:%s\ncatalog_worker:%s\nmount:%s\nsource_driver:%s\n",
		suiteVersion, catalog, catalogWorker, mount, sourceDriver,
	)
	digest := sha256.Sum256([]byte(raw))
	return "fusekit.transport." + hex.EncodeToString(digest[:])
}

func moduleRoot() string {
	_, file, _, ok := runtime.Caller(0)
	if !ok {
		panic("transportproto/gen: caller path unavailable")
	}
	return filepath.Clean(filepath.Join(filepath.Dir(file), "..", ".."))
}
